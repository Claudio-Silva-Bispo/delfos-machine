# DDL – Para usar no Data Modeler 

-- Tabela Cliente 

CREATE TABLE Cliente ( 

    ID_Cliente NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 

    Nome VARCHAR2(100) NOT NULL, 

    Email VARCHAR2(100) NOT NULL, 

    Telefone VARCHAR2(15), 

    Data_Nascimento DATE, 

    Endereço VARCHAR2(255) 

); 


-- Tabela Clínica 

CREATE TABLE Clínica ( 

    ID_Clínica NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 

    Nome VARCHAR2(100) NOT NULL, 

    Endereço VARCHAR2(255), 

    Telefone VARCHAR2(15), 

    Avaliação FLOAT, 

    Preço_Médio FLOAT 

); 

  
-- Tabela Consulta 

CREATE TABLE Consulta ( 

    ID_Consulta NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 

    ID_Cliente NUMBER, 

    ID_Clínica NUMBER, 

    Dentista_ID NUMBER, 

    Tipo_Serviço VARCHAR2(100), 

    Data_Consulta DATE, 

    Hora_Consulta DATE, 

    Tipo_Consulta VARCHAR2(50), 

    Local_Consulta VARCHAR2(255), 

    Status_Consulta VARCHAR2(20), 

    Observações VARCHAR2(250),

    Sintomas VARCHAR2(250),

    Tratamento_Recomendado VARCHAR2(250),

    Custo FLOAT, 

    Forma_Pagamento VARCHAR2(50), 

    Prescrição VARCHAR2(250),

    Data_Retorno DATE, 

    FOREIGN KEY (ID_Cliente) REFERENCES Cliente(ID_Cliente), 

    FOREIGN KEY (ID_Clínica) REFERENCES Clínica(ID_Clínica) 

); 

  
-- Tabela Sinistro 

CREATE TABLE Sinistro ( 

    ID_Sinistro NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 

    Nome VARCHAR2(100), 

    Descrição VARCHAR2(250),

    Status_Sinistro VARCHAR2(20), 

    Descricao_Status VARCHAR2(250),

    Valor_Sinistro FLOAT, 

    Data_Abertura DATE, 

    Data_Resolucao DATE, 

    Documentacao VARCHAR2(250), 

); 

  
-- Tabela Feedback 

CREATE TABLE Feedback ( 

    ID_Feedback NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 

    ID_Cliente NUMBER, 

    ID_Clínica NUMBER, 

    Avaliação FLOAT, 

    Comentário VARCHAR2(250),

    FOREIGN KEY (ID_Cliente) REFERENCES Cliente(ID_Cliente), 

    FOREIGN KEY (ID_Clínica) REFERENCES Clínica(ID_Clínica) 

); 

  
