CREATE TABLE Agenda ( 

    ID_Agenda NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 

    ID_Cliente NUMBER, 

    ID_Consulta NUMBER, 

    Data_Consulta DATE, 

    Hora_Consulta DATE, 

    Status_Consulta VARCHAR2(20), 

    Observacoes VARCHAR2(250),

    FOREIGN KEY (ID_Cliente) REFERENCES Cliente(ID_Cliente), 

    FOREIGN KEY (ID_Consulta) REFERENCES Consulta(ID_Consulta) 

); 


CREATE TABLE Notificacoes ( 

    ID_Notificacao NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 

    ID_Cliente NUMBER, 

    Tipo_Notificacao VARCHAR2(20), -- 'Email' ou 'WhatsApp' 

    Mensagem VARCHAR2(250), 

    Data_Envio DATE, 

    Status_Notificacao VARCHAR2(20), -- 'Enviada', 'Pendente', 'Falha' 

    FOREIGN KEY (ID_Cliente) REFERENCES Cliente(ID_Cliente) 

); 

 

Aqui estão os pontos que garantem a normalização dentro da terceira forma: 


1. Eliminação de Redundâncias 

Cada tabela representa uma entidade única (Cliente, Clínica, Consulta, Sinistro, Feedback, Formulário Detalhado), evitando a duplicação de informações. 

2. Dependências Funcionais 

Todos os atributos não-chave dependem exclusivamente da chave primária da tabela. Por exemplo: 

Na tabela Consulta, todos os atributos (como Tipo_Serviço, Data_Consulta, etc.) dependem do ID_Consulta. 

Na tabela Cliente, todos os atributos dependem do ID_Cliente. 

3. Atributos Multivalorados 

Não há atributos multivalorados. Cada campo contém apenas um valor por registro. 

4. Transitiva Dependência 

Não existem dependências transitivas. Todos os atributos são diretamente dependentes da chave primária, e não de outros atributos. 

 
